package entity;

public class LoginInfo {
	private String git_pwd;
	private String git_pwd_md5;
	private String id;
	private String login_name;
	private String opt_person;
	private String opt_time;
	private String state;
	private String svn_pwd;
	private String svn_pwd_md5;
	private String token_key;
	private String uid;
	
	public String getGit_pwd() {
		return git_pwd;
	}
	public void setGit_pwd(String git_pwd) {
		this.git_pwd = git_pwd;
	}
	public String getGit_pwd_md5() {
		return git_pwd_md5;
	}
	public void setGit_pwd_md5(String git_pwd_md5) {
		this.git_pwd_md5 = git_pwd_md5;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLogin_name() {
		return login_name;
	}
	public void setLogin_name(String login_name) {
		this.login_name = login_name;
	}
	public String getOpt_person() {
		return opt_person;
	}
	public void setOpt_person(String opt_person) {
		this.opt_person = opt_person;
	}
	public String getOpt_time() {
		return opt_time;
	}
	public void setOpt_time(String opt_time) {
		this.opt_time = opt_time;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getSvn_pwd() {
		return svn_pwd;
	}
	public void setSvn_pwd(String svn_pwd) {
		this.svn_pwd = svn_pwd;
	}
	public String getSvn_pwd_md5() {
		return svn_pwd_md5;
	}
	public void setSvn_pwd_md5(String svn_pwd_md5) {
		this.svn_pwd_md5 = svn_pwd_md5;
	}
	public String getToken_key() {
		return token_key;
	}
	public void setToken_key(String token_key) {
		this.token_key = token_key;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	
}
